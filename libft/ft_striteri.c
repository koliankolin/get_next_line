#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	int cnt;

	if (!s || !f)
		return ;
	cnt = 0;
	while (*s)
	{
		f(cnt, &*s++);
		cnt++;
	}
}
